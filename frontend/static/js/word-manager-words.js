/**
 * Call AI to generate definition and example for the entered word.
 * @tag MY_WORDS @tag AI
 */
async function wmGenerateAI() {
  var word = _wmEl("wm-word").value.trim();
  var pos = _wmEl("wm-pos").value.trim();
  if (!word) { _wmEl("wm-word").focus(); return; }

  var btn = _wmEl("wm-btn-ai");
  var result = _wmEl("wm-ai-result");
  btn.disabled = true;
  btn.textContent = "Generating...";
  result.classList.add("hidden");

  try {
    var data = await _wmApi(_WM_API + "/ai-enrich", {
      method: "POST",
      body: JSON.stringify({ word: word, pos: pos }),
    });

    var aiPos = data.pos ? (Array.isArray(data.pos) ? data.pos[0] : data.pos) : "";

    if (data.definition) {
      result.classList.remove("hidden");
      result.innerHTML =
        '<div class="wm-ai-card">' +
          '<div class="wm-ai-field">' +
            '<div class="wm-ai-label">DEFINITION</div>' +
            '<textarea id="wm-ai-def" class="wm-input wm-ai-input wm-textarea" rows="2">' +
              _wmEsc(data.definition) +
            "</textarea>" +
          "</div>" +
          '<div class="wm-ai-field">' +
            '<div class="wm-ai-label">EXAMPLE</div>' +
            '<textarea id="wm-ai-example" class="wm-input wm-ai-input wm-textarea" rows="2">' +
              _wmEsc(data.example) +
            "</textarea>" +
          "</div>" +
          (aiPos && !pos
            ? '<div class="wm-ai-field wm-ai-field-half">' +
                '<div class="wm-ai-label">PART OF SPEECH</div>' +
                '<input type="text" id="wm-ai-pos" class="wm-input wm-ai-input" value="' + _wmEsc(aiPos) + '" />' +
              "</div>"
            : "") +
          '<div class="wm-ai-provider">Generated by ' + _wmEsc(data.provider) + "</div>" +
        "</div>";
    } else {
      result.classList.remove("hidden");
      result.innerHTML = '<div class="wm-ai-fail">AI couldn\'t generate. Please enter manually.</div>';
    }
  } catch (err) {
    result.classList.remove("hidden");
    result.innerHTML = '<div class="wm-ai-fail">Error: ' + _wmEsc(err.message) + "</div>";
  } finally {
    btn.disabled = false;
    btn.textContent = "Generate with AI";
  }
}

/**
 * Add the current form word to the active lesson.
 * @tag MY_WORDS
 */
async function wmAddWord() {
  var word = _wmEl("wm-word").value.trim();
  if (!word) { _wmEl("wm-word").focus(); return; }

  var definition = "", example = "", pos = _wmEl("wm-pos").value.trim();

  if (_wmState.mode === "ai") {
    var aiDef = _wmEl("wm-ai-def");
    var aiEx = _wmEl("wm-ai-example");
    var aiPos = _wmEl("wm-ai-pos");
    definition = aiDef ? aiDef.value.trim() : "";
    example = aiEx ? aiEx.value.trim() : "";
    if (aiPos && !pos) pos = aiPos.value.trim();
    if (!definition) {
      toast("Generate AI definition first, or switch to Direct Input.", "warn");
      return;
    }
  } else {
    definition = _wmEl("wm-def").value.trim();
    example = _wmEl("wm-example").value.trim();
    if (!definition) { _wmEl("wm-def").focus(); return; }
  }

  var btn = _wmEl("wm-btn-add");
  btn.disabled = true;
  btn.textContent = "Adding...";

  try {
    await _wmApi(_WM_API + "/" + encodeURIComponent(_wmState.currentLesson) + "/words", {
      method: "POST",
      body: JSON.stringify({ word: word, pos: pos, definition: definition, example: example }),
    });

    _wmEl("wm-word").value = "";
    _wmEl("wm-pos").value = "";
    if (_wmEl("wm-def")) _wmEl("wm-def").value = "";
    if (_wmEl("wm-example")) _wmEl("wm-example").value = "";
    _wmEl("wm-ai-result").classList.add("hidden");
    _wmEl("wm-ai-result").innerHTML = "";
    _wmEl("wm-word").focus();

    await wmLoadWords();
  } catch (err) {
    toast(err.message, "error");
  } finally {
    btn.disabled = false;
    btn.textContent = "+ Add Word";
  }
}

/**
 * Fetch words for the current lesson and refresh the rendered list.
 * @tag MY_WORDS
 */
async function wmLoadWords() {
  try {
    var res = await fetch("/api/study/English/My_Words/" + encodeURIComponent(_wmState.currentLesson));
    if (!res.ok) { _wmState.words = []; wmRenderWords(); return; }
    var data = await res.json();
    _wmState.words = (data.items || []).map(function (it) {
      var extra = {};
      try { extra = JSON.parse(it.extra_data || "{}"); } catch (e) {}
      return {
        id: it.id,
        word: it.answer,
        definition: it.question,
        example: it.hint || "",
        pos: extra.pos || "",
      };
    });
    wmRenderWords();
  } catch (err) {
    _wmState.words = [];
    wmRenderWords();
  }
}

/**
 * Render the word list inside the lesson detail view.
 * @tag MY_WORDS
 */
function wmRenderWords() {
  var list = _wmEl("wm-wordlist");
  _wmEl("wm-word-count").textContent = _wmState.words.length;

  if (!_wmState.words.length) {
    list.innerHTML = '<div class="wm-empty-words">No words yet. Start adding!</div>';
    return;
  }

  list.innerHTML = _wmState.words.map(function (w, i) {
    return '<div class="wm-word-item">' +
      '<div class="wm-word-num">' + (i + 1) + "</div>" +
      '<div class="wm-word-main">' +
        '<div class="wm-word-text">' + _wmEsc(w.word) +
          ' <span class="wm-word-pos">' + _wmEsc(w.pos) + "</span></div>" +
        '<div class="wm-word-def">' + _wmEsc(w.definition) + "</div>" +
        (w.example ? '<div class="wm-word-ex">' + _wmEsc(w.example) + "</div>" : "") +
      "</div>" +
      '<button class="wm-word-del" data-word="' + _wmEsc(w.word) + '" title="Delete">' +
        '<i data-lucide="x" style="width:14px;height:14px;pointer-events:none"></i>' +
      "</button>" +
    "</div>";
  }).join("");

  list.querySelectorAll(".wm-word-del").forEach(function (btn) {
    btn.onclick = function () { wmDeleteWord(btn.dataset.word); };
  });
  if (typeof lucide !== "undefined") lucide.createIcons();
}

/**
 * Delete a single word from the current lesson.
 * @tag MY_WORDS
 */
async function wmDeleteWord(word) {
  if (!confirm('Delete "' + word + '"?')) return;
  try {
    await _wmApi(
      _WM_API + "/" + encodeURIComponent(_wmState.currentLesson) +
        "/words/" + encodeURIComponent(word),
      { method: "DELETE" }
    );
    await wmLoadWords();
  } catch (err) {
    toast(err.message, "error");
  }
}

/**
 * Start the learn flow for a My Words lesson (Preview modal → SM-2 register).
 * @tag MY_WORDS @tag REVIEW @tag SM2
 */
async function wmStartLearning(lessonName) {
  var wordList = [];
  try {
    var res = await fetch("/api/study/English/My_Words/" + encodeURIComponent(lessonName));
    if (!res.ok) throw new Error("HTTP " + res.status);
    var data = await res.json();
    wordList = (data.items || []).map(function (it) {
      return {
        id: "mw-" + lessonName + "-" + it.id,
        answer: it.answer,
        question: it.question || "",
        hint: it.hint || "",
      };
    });
  } catch (err) {
    toast("Failed to load words: " + err.message, "error");
    return;
  }
  if (!wordList.length) { toast("No words in this list.", "warn"); return; }
  if (typeof window.openPreviewModal !== "function") {
    toast("Preview module not loaded.", "error"); return;
  }

  var ov = _wmEl("wm-overlay");
  ov.classList.add("hidden");

  var learned = [];
  var idx = 0;

  var restoreAcademy = (function () {
    var prevItems = window.items;
    var prevStage = window.stage;
    return function () { window.items = prevItems; window.stage = prevStage; };
  })();

  function next() {
    if (idx >= wordList.length) { finish(); return; }
    var item = wordList[idx];
    window.items = [item];
    if (typeof previewDoneMap !== "undefined") previewDoneMap.clear();
    window.stage = "mywords";
    window.openPreviewModal(item, function (status) {
      if (status === "ok" || status === "auto-pass") {
        learned.push({ word: item.answer, question: item.question, hint: item.hint });
        idx++;
        next();
      } else {
        finish();
      }
    });
  }

  async function finish() {
    restoreAcademy();
    if (learned.length > 0) {
      try {
        await _wmApi("/api/review/register-words", {
          method: "POST",
          body: JSON.stringify({ source: "my", source_ref: lessonName, words: learned }),
        });
      } catch (err) { console.warn("[MyWords] register-words failed", err); }
    }
    ov.classList.remove("hidden");
    if (typeof window.refreshReviewBadge === "function") window.refreshReviewBadge();
  }

  next();
}
